                <h4 class="text-center">Game Rules (Barbarian attack)</h4>
                <p>
                    Barbarian Attack:
                    <br>
                    <li>Place the inner terrain hexes: Randomly place the following
                        hexes in the white inner area: 1 forest, 1 pasture, 1 hills, 2 mountains
                        , and 2 fields. One forest hex is not used
                    </li>
                    <li>Place the inner terrain hexes: Randomly place the following
                        hexes in the white inner area: 1 forest, 1 pasture, 1 hills, 2 mountains
                        , and 2 fields. One forest hex is not used
                    </li>
                    <li>Take the 6 knights of your colour</li>
                    <li>Place a barbarian on the hex with the "2" number token and on the hex
                        with the "12." The remaining barbarians are placed beside the game board.
                    </li>
                    <li>Use the new 26 development cards that this expansion provides for this scenario. 
                        Do not use any of the Catan base game development cards.
                    </li>
                    <li>Do not use the "Largest Army" special card</li>
                    <li>Do not use the robber</li>
                </p>
