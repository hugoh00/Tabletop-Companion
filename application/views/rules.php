            <div class="container rounded col-sm px-2 m-2" style="border:1px solid black">
                <h4 class="text-center">Barbarians Land on Catan (Barbarian Attack)</h4>
                <p>
                    In addition to rolling for production, trading and building, you may also resolve
                    the following actions during your turn:
                    <li>1. Barbarians land in Catan (each time you build a settlement or upgrade to a city)</li>
                    <li>2. Immediately play a development card (each time you buy a development card)</li>
                    <li>3. Place a knight (each time you play a "Knighthood" or a "Swift Knight" development card)</li>
                    <li>4. Move your knights (at the end of your turn, after you finish trading and building)</li>
                    <li>5. Expel the barbarians (at the end of your turn, after moving your knights)</li>
                </p>
                <br>
                <p>
                    1. Barbarian Attack:
                    (Proceed as follows)
                    <li>Roll the dice until you get a result that is not a "7" (re-roll as many times)
                        Then place a barbarian on the coastal hex with the number token of the dice roll result.
                        If this coastal hex already has 3 barbarians, do not place a barbarian or re-roll the dice.
                    </li>
                    <li>
                        Roll the dice until you get a result that is not "7" and is not the same as the first result.
                        Then use this dice result to place a barbarian as outlined above.
                    </li>
                    <li>Roll the dice until you get a result that is not "7" and is not the same as either
                        of the first two dice roll results. Then use this dice roll result to place a barbarian as outlined in the 1st procedure.
                    </li>
                </p>
                <p>
                    2. Coastal hex is conquered
                    <li>3 barbarians on a coastal hex means the token should be faced down</li>
                    <li>It no longer produces resources when you roll its number. You no longer place barbarins on this hex during an attack</li>
                    <li>You may not build a road on a path adjacent to a conquered hex</li>
                    <li>You may not build a settlement on an intersection adjeacent to conquered hex</li>
                </p>
                <p>
                    3. A settlement or city is conquered
                    <li>As soon as a city is not adjacent to an unconquered hex. It is conquered</li>
                    <li>You get no victory points for a conquered settlement/city</li>
                    <li>If a conquered settlement/city has a harbour, you cannot use it</li>
                    <li>Since the desert hex and castle hex can never be conquered, a settlement or city adjacent to one of these cannot be conquered</li>
                </p>

            </div>
            <div class="container rounded col-sm px-2 m-2" style="border:1px solid black">
                <h4 class="text-center">Traders & Barbarians</h4>
                <p>
                    Preparation:
                    <li>First, assemble the frame. Replace 2 frame pieces from basic Catan with the corresponding in the expansion</li>
                    <li>Then place the 3 trade hexes. The sea side of each trade hex must match up with the sea side of each frame.
                        Do not use the following 3 hexes from the base game: desert hex, 1 pasture hex and 1 fields hex.
                    </li>
                    <li>Do not place number tokens on the 3 trade hexes - remove the "2" and "12" number tokens from play. 
                        Place the remaining number tokens as specified. Skipping the trade hexes.
                    </li>
                    <li>Arrange the commodity tokens into 3 stacks sorted by pictures on their backs (castle, quarry, glassworks). Shuffle
                        each stack and place it with the backs facing up (showing the picture of a building) beside the corresponding
                        trade hex.
                    </li>
                    <li>You recieve the set of 5 baggage train cards whose frame colours match your colour, stacking face down and sorted in the sequence of 1-5. Turn the top baggage train card of your
                        stack face up and place it next to the stack. This is your active baggage train card.
                    </li>
                    <br>
                    <li>Place the 3 barbarians on the paths marked with black crosses</li>
                    <li>Use the new 25 development cards that this expansion provides. Not using any of the base game development cards</li>
                    <li>You recieve 5 gold</li>
                    <li>Do not use the "Longest Road" special card</li>
                    <li>Do not use the robber</li>
                </p>
                <br>
                <p>
                    Trade Hexes:
                    <li>Each of the 3 trade hexes has a "central plaza" intersection with a building.
                        Four "interior" paths lead to this central plaza.
                    </li>
                    <li>You may build roads on interior paths, according to usual rules</li>
                    <li>You may not build a settlement on the central plaza intersection of a trade hex</li>
                    <li>You may not build roads on the three paths of a trade hex that border on the sea</li>
                    <li>You may not build settlements/cities on the 4 land corners of a trade hex</li>
                    <br>
                    Moving your wagon:
                    These apply after finishing up trading and building during your turn
                    <li>You move along paths from intersection to adjacent intersection. This costs 1 or more "movement points" (MPs)</li>
                    <li>Any number of wagons may occupy a given intersection</li>
                    <li>At the beginning of the game, your wagon has 4 MPs to use each time your take your turn. You can increase MPs by upgrading your train</li>
                    <li>The MP cost = 2 if the path does NOT have a road</li>
                    <li>The MP cost = 1 if the path has one of YOUR own roads</li>
                    <li>The MP cost = 1 if the path has ANOTHER player's road but you must also pay 1 gold to that player</li>
                    <li>The MP cost is increased by an additional +2 if there is a barbarian on the path</li>
                    <li>You may pay 1 (only 1) grain to increase MP by +2 for current turn</li>
                    <li>Your wagon may stop and end its movement on any place it moves to. Unused MP is lost</li>
                    <li>Your wagon must stop and end its movement whenever it moves onto the central plaza of a trade hex</li>
                    <li>If you do not have enough unused MPs to move along a path, you may not partially move along the path</li>
                    <br>
                    Choosing an initial destination trade hex:
                    <li>Choose one of 3 trade hexes to move towards when you first move</li>
                    <li>You may change your destination at will</li>
                    <li>When you eventually reach the central plaza of the trade hex. You don't recieve gold. However, you do recieve the top commodity token from the stack corresponding to the trade hex.
                        Turn it to face up in front of yourself to reveal the commodity depicted on the front of the token.
                    </li>
                    <li>Later on, you must move your wagon to the trade hex where this commodity can be delivered. Glass and marble to the castle, tools to the quarry and sand to the glassworks.</li>
                    <li>You can only carry out one delivery at a time. Only after this is done may you draw another commodity token</li>
                    <br>
                    Delivering your commodity:
                    <li>Turn the commodity token face down. The token is 1 victory point</li>
                    <li>Depending on your baggage train, you may also recieve 1 - 5 gold</li>
                    <li>Take and reveal another commodity token as outlined before</li>
                    <br>
                    Upgrading your baggage train:
                    <li>During trading and building, you may pay resources to upgrade to your next baggage train card</li>
                    <li>Pay the resources shown at the top card of your baggage train stack</li>
                    <li>Turn the top card of that stack over and place it on the top of your baggage train card.</li>
                    <li>Upgrading to the last baggage train card is worth 1 victory point.</li>
                    <br>
                    The barbarians:
                    <li>When moving past, your movement cost is 2 more than normally required</li>
                    <li>If you upgrade your train card at least once, you can attempt to drive off the barbarian. Roll one die.</li>
                    <li>"7" is rolled: you must move one of the 3 barbarians to any path not already occupied by a barbarian. If moved to  a path occupied by a road. You may draw a resource card (not gold) from the owner. 
                        In addition, each player who has more than 7 resource cards must select half (rounded down) of their resource cards and return them to the bank.
                    </li>
                    <li>
                        A "2" or a "12" is rolled means you must re-roll the dice
                    </li>
                </p>    
